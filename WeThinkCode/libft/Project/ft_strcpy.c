#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	int count;

	count = 0;
	while (s2[count] != '\0')
	{
		s1[count] = s2[count];
		++count;
	}
	s1[count] = '\0';
	return (s1);
}
